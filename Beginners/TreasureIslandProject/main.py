print("Welcome to Treasure Island.")
print("Your mission is to find the treasure.")
direction = input("\nDo you want to take right of left?")

if direction.lower() == "left":
    choice = input("\nThere is lake ahead, Do you want to swim or wait?")
    if choice.lower() == "wait":
        door = input("\nThere is 3 doors red, yellow and blue. select any one.")
        if door.lower() == "red":
            print("\nYou are burned by fire. Game over...")
        elif door.lower() == "blue":
            print("\nYou are eaten by beasts. Game over...")
        elif door.lower() == "yellow":
            print("\nYou did it! You have found the treasure. Congratulations")
        else:
            print("\nGame over...")
    else:
        print("\nYou are attacked by the trout. Game over...")
else:
    print("\nFall into a hole. Game over...")
